package in.completecourse.questionbank.model;

public class Subject {
    private String mSubjectName;
    private int mIconResourceId;
    private String mClass;


    public String getmSubjectName() {
        return mSubjectName;
    }

    public void setmSubjectName(String mSubjectName) {
        this.mSubjectName = mSubjectName;
    }

    public int getmIconResourceId() {
        return mIconResourceId;
    }

    public void setmIconResourceId(int mIconResourceId) {
        this.mIconResourceId = mIconResourceId;
    }

    public String getmClass() {
        return mClass;
    }

    public void setmClass(String mClass) {
        this.mClass = mClass;
    }
}
